const inicio = 7
const final = 12

// CON FOR
console.log("---------- CON FOR ------------")
for(let i = inicio; i<=final; i++){
    console.log(i)
}

// CON WHILE
console.log("---------- CON WHILE ------------")
let contador = 7
while(contador<=final){
    console.log(contador)
    contador++
}
