const inicio = 1
const final = 17

// CON FOR
console.log("---------- CON FOR ------------")
for(let i = inicio; i<=final; i++){
    console.log(i)
}

// CON WHILE
console.log("---------- CON WHILE ------------")
let contador = 1
while(contador<=final){
    console.log(contador)
    contador++
}
